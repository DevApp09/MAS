        [RequestDirectTo("SurveyGetDropDownList")]
        public async Task<string> SurveyGetDropDownList(HttpContext context)
        {
            try
            {
                if (!StrongTypeSession.UserAutoGeneratedId.HasValue)
                {
                    return ServiceResponse.AuthenticationErrorResponse;
                }
                else
                {
                    var jObject = JsonConvert.DeserializeObject(_postedJson) as JObject;
                    bool? isActive = ((JValue)jObject["isActive"]).Value<bool?>();
                    int? defaultKey = ((JValue)jObject["defaultKey"]).Value<int?>();
                    string defaultValue = ((JValue)jObject["defaultValue"]).Value<string>();

                    ISurveyBLL bll = IOC.Resolve<ISurveyBLL>();
                    IEnumerable<KeyValuePair<long, string>> entities = await bll.GetAllKeyValuePairAsync(isActive, defaultKey, defaultValue);
                    return ServiceResponse.GetOKResponse(entities);
                }
            }
            catch (DALException ex)
            {
                return ServiceResponse.GetErrorResponse(ErrorClass.DAL, ex.Message, null);
            }
            catch (BLLException ex)
            {
                return ServiceResponse.GetErrorResponse(ErrorClass.BLL, ex.Message, null);
            }
            catch (Exception ex)
            {
                _logger.Error("Error", ex);
                return ServiceResponse.UnExpectedErrorResponse;
            }
        }

