                }
                else
                {
                    var jObject = JsonConvert.DeserializeObject(_postedJson) as JObject;
                    long surveyID = ((JValue)jObject["SurveyID"]).Value<long>();

                    var connectionString = ConfigurationManager.ConnectionStrings["MASConnectionString"].ToString();
                    var parameters = new SqlParameter[]
                        {
                                    new SqlParameter("@SurveyID", surveyID){SqlDbType = SqlDbType.BigInt},
                        };
                    string query = "SP_SurveyReport";
                    var dta = SqlHelper.GetDataSetFromSP(connectionString, query, parameters);
                    var json = JSONSerializer.ToJson(dta);
                    return ServiceResponse.GetOKResponse(json);
                }
            }
            catch (MAS.Exceptions.DALException ex)
            {
